export function getTranslations(lang: Language) {
  const base = translations[lang] || translations.en;
  
  // Add category translations
  return {
    ...base,
    categories: {
      food: base.categoryFood,
      movie: base.categoryMovie,
      together: base.categoryTogether,
      intimacy: base.categoryIntimacy,
      world: base.worldTitle
    }
  };
}

// Alias for backward compatibility
export const getTranslation = getTranslations;

// Supported language type alias
export type SupportedLanguage = Language;

// Detect language from browser or default
export function detectLanguage(acceptLanguage?: string): Language {
  if (!acceptLanguage) return 'en';
  
  const lang = acceptLanguage.split(',')[0].split('-')[0].toLowerCase();
  
  if (lang === 'de') return 'de';
  if (lang === 'es') return 'es';
  if (lang === 'fr') return 'fr';
  if (lang === 'it') return 'it';
  
  return 'en';
}
